
public class Category {
    private String categoryID;
    private String categoryName;
    private String categoryStandNumber;
    private String categoryStandAdress;

    public String getCategoryID() {
        return categoryID;
    }

    public void setCategoryID(String categoryID) {
        this.categoryID = categoryID;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }

    public String getCategoryStandNumber() {
        return categoryStandNumber;
    }

    public void setCategoryStandNumber(String categoryStandNumber) {
        this.categoryStandNumber = categoryStandNumber;
    }

    public String getCategoryStandAdress() {
        return categoryStandAdress;
    }

    public void setCategoryStandAdress(String categoryStandAdress) {
        this.categoryStandAdress = categoryStandAdress;
    }
    
}
